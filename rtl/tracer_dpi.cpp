static std::string build_disp_str(const std::string& decode_str, const RvfiData& d,
                                  uint16_t data_accessed) {
  std::ostringstream oss;
  //  oss << std::hex << std::nouppercase << d.pc_rdata << "\t";
  oss << std::hex  << std::nouppercase  << std::setw(8) << std::setfill('0') << d.pc_rdata << "\t";

  if ((d.insn & 0x3u) != 0x3u) {
    oss << "    " << std::setw(4) << std::setfill('0') << (d.insn & 0xffffu);
  } else {
    oss << std::setw(8) << std::setfill('0') << d.insn;
  }
  oss << "\t";

  if (d.trap) oss << "-->";
  oss << decode_str << "\t";

  auto add_reg_read = [&](uint8_t addr, uint32_t lo) {
    oss << " " << reg_addr_to_str(addr) << ":0x"
        << std::setw(8) << std::setfill('0') << lo;
  };
  auto add_cap_read = [&](uint8_t addr, uint32_t lo, uint64_t hi) {
    oss << " " << reg_addr_to_str(addr) << ":0x"
        << std::setw(8) << std::setfill('0') << lo
        << "+0x" << std::setw(9) << std::setfill('0') << hi;
  };
  auto add_reg_write = [&](uint8_t addr, uint32_t lo) {
    oss << " " << reg_addr_to_str(addr) << "=0x"
        << std::setw(8) << std::setfill('0') << lo;
  };
  auto add_cap_write = [&](uint8_t addr, uint32_t lo, uint64_t hi) {
    oss << " " << reg_addr_to_str(addr) << "=0x"
        << std::setw(8) << std::setfill('0') << lo
        << "+0x" << std::setw(9) << std::setfill('0') << hi;
  };


  if (data_accessed & RS1) add_reg_read(d.rs1_addr, reg_lo32(d.rs1_rdata_lo));
  if (data_accessed & CS1) add_cap_read(d.rs1_addr, reg_lo32(d.rs1_rdata_lo), d.rs1_rdata_hi);
  if (data_accessed & RS2) add_reg_read(d.rs2_addr, reg_lo32(d.rs2_rdata_lo));
  if (data_accessed & CS2) add_cap_read(d.rs2_addr, reg_lo32(d.rs2_rdata_lo), d.rs2_rdata_hi);
  if (data_accessed & RS3) add_reg_read(d.rs3_addr, reg_lo32(d.rs3_rdata_lo));
  if (data_accessed & RD)  add_reg_write(d.rd_addr, reg_lo32(d.rd_wdata_lo));
  if (data_accessed & CD)  add_cap_write(d.rd_addr, reg_lo32(d.rd_wdata_lo), d.rd_wdata_hi);

  if (data_accessed & MEM) {
    oss << " PA:0x" << std::setw(8) << std::setfill('0') << d.mem_addr;
    if (d.mem_wmask == 0x1) {
      oss << " store:0x??????" << std::setw(2) << std::setfill('0') << (d.mem_wdata_lo & 0xffu);
    } else if (d.mem_wmask == 0x3) {
      oss << " store:0x????" << std::setw(4) << std::setfill('0') << (d.mem_wdata_lo & 0xffffu);
    } else if (d.mem_wmask != 0) {
      oss << " store:0x" << std::setw(8) << std::setfill('0') << reg_lo32(d.mem_wdata_lo);
    }
    if (d.mem_rmask != 0) {
      oss << " load:0x" << std::setw(8) << std::setfill('0') << reg_lo32(d.mem_rdata_lo);
    }
  }

  if (data_accessed & MEMC) {
    oss << " PA:0x" << std::setw(8) << std::setfill('0') << d.mem_addr;
    if (d.mem_wmask != 0) {
      oss << " store:0x" << std::setw(8) << std::setfill('0') << reg_lo32(d.mem_wdata_lo)
          << "+0x" << std::setw(9) << std::setfill('0') << d.mem_wdata_hi;
    } else {
      oss << " load:0x" << std::setw(8) << std::setfill('0') << reg_lo32(d.mem_rdata_lo)
          << "+0x" << std::setw(9) << std::setfill('0') << d.mem_rdata_hi;
      //printf("PA = %x, mem_rdata_hi = %x\n", d.mem_addr, d.mem_rdata_hi);
    }
  }

  return oss.str();
}

static std::string finish(const std::string& s, const RvfiData& d, uint16_t access) {
  return build_disp_str(s, d, access);
}

