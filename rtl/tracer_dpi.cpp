static std::string decode_cb_insn(const char* m, const RvfiData& d) {
  if (bits(d.insn,15,13) == 0b110 || bits(d.insn,15,13) == 0b111) {
    uint32_t imm8 = (bits(d.insn,12,12)<<7) | (bits(d.insn,6,5)<<5) |
                    (bits(d.insn,2,2)<<4) | (bits(d.insn,11,10)<<2) |
                    bits(d.insn,4,3);
    uint32_t target = d.pc_rdata + static_cast<uint32_t>(sext((imm8<<1),9));
    return finish(fmt("%s\tx%u,%x", m, d.rs1_addr, target), d, RS1);
  }
  if (bits(d.insn,15,13) == 0b100) {
    uint32_t imm = (bits(d.insn,12,12)<<5) | bits(d.insn,6,2);
    return finish(fmt("%s\tx%u,%d", m, d.rd_addr, sext(imm,6)), d, RS1|RD);
  }
  uint32_t imm = (bits(d.insn,12,12)<<7) | (bits(d.insn,6,2)<<2);
  return finish(fmt("%s\tx%u,0x%x", m, d.rs1_addr, imm), d, RS1);
}
