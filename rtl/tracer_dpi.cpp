extern "C" const char* decode_rvfi_instr(
    uint32_t insn,
    uint8_t trap,
    uint8_t intr,
    uint8_t rs1_addr,
    uint8_t rs2_addr,
    uint8_t rs3_addr,
    uint32_t rs1_rdata_lo,
    uint32_t rs2_rdata_lo,
    uint32_t rs3_rdata_lo,
    uint64_t rs1_rdata_hi,
    uint64_t rs2_rdata_hi,
    uint64_t rs3_rdata_hi,
    uint8_t rd_addr,
    uint32_t rd_wdata_lo,
    uint64_t rd_wdata_hi,
    uint32_t pc_rdata,
    uint32_t pc_wdata,
    uint32_t mem_addr,
    uint8_t mem_rmask,
    uint8_t mem_wmask,
    uint32_t mem_rdata_lo,
    uint32_t mem_wdata_lo,
    uint64_t mem_rdata_hi,
    uint64_t mem_wdata_hi,
    uint8_t cheri_pmode) {
  static thread_local std::string result;
  tracer_dpi::RvfiData d{};
  d.insn = insn;
  d.trap = trap != 0;
  d.intr = intr != 0;
  d.rs1_addr = rs1_addr;
  d.rs2_addr = rs2_addr;
  d.rs3_addr = rs3_addr;
  d.rs1_rdata_lo = rs1_rdata_lo;
  d.rs2_rdata_lo = rs2_rdata_lo;
  d.rs3_rdata_lo = rs3_rdata_lo;
  d.rs1_rdata_hi = rs1_rdata_hi;
  d.rs2_rdata_hi = rs2_rdata_hi;
  d.rs3_rdata_hi = rs3_rdata_hi;
  d.rd_addr = rd_addr;
  d.rd_wdata_lo = rd_wdata_lo;
  d.rd_wdata_hi = rd_wdata_hi;
  d.pc_rdata = pc_rdata;
  d.pc_wdata = pc_wdata;
  d.mem_addr = mem_addr;
  d.mem_rmask = mem_rmask;
  d.mem_wmask = mem_wmask;
  d.mem_rdata_lo = mem_rdata_lo;
  d.mem_wdata_lo = mem_wdata_lo;
  d.mem_rdata_hi = mem_rdata_hi;
  d.mem_wdata_hi = mem_wdata_hi;
  d.cheri_pmode = cheri_pmode != 0;

  //printf("rs1_rdata_lo = %x, rx1_rdata_hi = %x\n", d.rs1_rdata_lo, d.rs1_rdata_hi);
  result = tracer_dpi::decode_rvfi_instr_cpp(d);
  return result.c_str();
}
